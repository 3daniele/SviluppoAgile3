    public function addMusicToPlaylist(Request $request, $id) {
    
        if (!(Playlist::where([['music_id', $request->uri],["hosting_id",$id]])->exists())) {
            $playlist=new Playlist;
            $playlist->hosting_id=$id;
            $playlist->music_id=$request->uri;
            $playlist->save();
        }  
